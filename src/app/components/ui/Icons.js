/**
 * Componente Icon que puede ser utilizado con nombres de iconos
 * Permite personalizar el tamaño y otras clases
 * @param {string} name - Nombre del icono a mostrar
 * @param {string} className - Clases CSS adicionales
 */
export default function Icon({ name, className = "" }) {
    // Si el icono existe, lo devuelve con la clase personalizada
    if (Icons[name]) {
        // Clonar el elemento para aplicar las clases personalizadas
        return React.cloneElement(Icons[name], {
            className: className || Icons[name].props.className,
        });
    }

    // Si el icono no existe, devuelve un placeholder
    return (
        <svg className={className || "h-5 w-5"} fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 16h-1v-4h-1m1-4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z" />
        </svg>
    );
}